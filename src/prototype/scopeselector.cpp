#include "ino/base/defaults.h"
#include "ino/scope/ScopeSelector.h"

using namespace ino;

/************************************************************************/
/* ScopeSelector                                                        */
/************************************************************************/
//------------------------------------------------------------------------
ScopeSelector::ScopeSelector()
//------------------------------------------------------------------------
{

}

//------------------------------------------------------------------------
ScopeSelector::ScopeSelector( const ScopeSelector & other )
//------------------------------------------------------------------------
{
	m_v = other.m_v;
}

//------------------------------------------------------------------------
ScopeSelector::~ScopeSelector()
//------------------------------------------------------------------------
{
}

//------------------------------------------------------------------------
size_t ScopeSelector::getSize() const
//------------------------------------------------------------------------
{
	return m_v.size();
}

//------------------------------------------------------------------------
size_t ScopeSelector::getTokensSize() const
//------------------------------------------------------------------------
{
	return m_v.size();
}

//------------------------------------------------------------------------
U32 ScopeSelector::getCount() const
//------------------------------------------------------------------------
{
	return m_v[0];
}

//------------------------------------------------------------------------
const U32 * ScopeSelector::getTokens() const
//------------------------------------------------------------------------
{
	return &m_v[1];
}

//------------------------------------------------------------------------
ScopeSelector & ScopeSelector::operator=( const ScopeSelector & other )
//------------------------------------------------------------------------
{
	m_v = other.m_v;
	return *this;
}
